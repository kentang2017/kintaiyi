def milestones():
    return list(_MILESTONES)


def for_year(year: int) -> dict:
    rugua = config.tongyun_rugua(year)
    bian = tongyun_bian_gua(year)
    exact = [m for m in _MILESTONES if m["年"] == year]
    same_gua = [
        m for m in _MILESTONES
        if m["卦"] == rugua["卦"] and m["爻"] == rugua["爻名"]
    ]
    near = sorted(_MILESTONES, key=lambda m: abs(m["年"] - year))[:3]
    return {
        "公元年": year,
        "統運入卦": rugua,
        "變卦納甲": bian,
        "當年例": exact,
        "同卦爻例": same_gua[:5],
        "近例": near,
    }


def zonghe(year: int) -> dict:
    info = for_year(year)
    rugua = info["統運入卦"]
    if info["當年例"]:
        tip = info["當年例"][0]["摘要"]
    elif info["同卦爻例"]:
        tip = f"同{rugua['卦']}{rugua['爻名']}：{info['同卦爻例'][0]['摘要']}"
    else:
        tip = (
            f"統運{rugua['運']}·{rugua['卦']}{rugua['爻名']}；"
            f"變卦{info['變卦納甲']['變卦']}"
        )
    return {
        "行支編年": info,
        "歷史驗例": _MILESTONES,
        "要訣": tip,
    }
